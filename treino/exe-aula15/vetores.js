let vet = [10, 11, 12, 13];
console.log(vet); // mostra o vetor
vet.push(1); // add um número em qualquer possição do vetor
console.log(vet); // mostra o vetor
vet.sort(); // coloca o vetor em ordem
console.log(vet); // mostra o vetor
console.log(`O vetor tem ${vet.length} posições`); // length mostra quantas posições tem o vetor!!
console.log(`o vetor na posicão 0 é ${vet[0]}`); // mostra o vetor na posição [0]!!
vet[6] = 14; // add o número na posição indicada
console.log(vet); // mostra o vetor
console.log(vet.indexOf(13)); // retorna a possição do valor
